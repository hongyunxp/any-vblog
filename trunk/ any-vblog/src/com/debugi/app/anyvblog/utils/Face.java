	/**
	 * 通过名称获取图片资源ID
	 * @param name 图片名称
	 * @return
	 */
	public static int getResourceId(String name) {
		int rid = 0;
		
		if (Face_List.get(name) != null) {
			rid = Face_List.get(name); 
		}
		return rid;
	}
	/**
	 * 通过资源id 获取名称
	 * @param resourceId 资源id
	 * @return
	 */
	public static String getName(int resourceId) {
		String name = "";
		for (Map.Entry<String, Integer> entry : Face_List.entrySet()) {
			if (entry.getValue() == resourceId) {
				name = entry.getKey();
			}
		}
		return name;
	}
	/**
	 * 获取所有表情资源数组
	 * @return
	 */
	public static int[] getAll() {
		int[] faces = new int[Face_List.size()];
		int i = 0;
		for (Map.Entry<String, Integer> entry : Face_List.entrySet()) {
			faces[i] = entry.getValue();
			i ++;
		}
		return faces;
	}
